package objects;

public class ReviewerPriorities {
    private int pr1, pr2, pr3;

    public ReviewerPriorities(int pr1, int pr2, int pr3) {
        this.pr1 = pr1;
        this.pr2 = pr2;
        this.pr3 = pr3;
    }

    public int getPr1() {
        return pr1;
    }

    public int getPr2() {
        return pr2;
    }

    public int getPr3() {
        return pr3;
    }
}
